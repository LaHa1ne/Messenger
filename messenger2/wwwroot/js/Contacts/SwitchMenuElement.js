function SwitchMenuElement(obj) {
    switch ($(obj).attr("id")) {
        case 'button_friends':
            $.ajax({
                url: '/Contacts/GetFriendsList',
                type: "GET",
                success: function (data) {
                    $("#Contacts_ContentBlock").html(data);
                },
                error: function (jqXHR, exception) {
                    var msg = '';
                    if (jqXHR.status === 0) {
                        msg = 'Not connect.\n Verify Network.';
                    } else if (jqXHR.status == 404) {
                        msg = 'Requested page not found. [404]';
                    } else if (jqXHR.status == 500) {
                        msg = 'Internal Server Error [500].';
                    } else if (exception === 'parsererror') {
                        msg = 'Requested JSON parse failed.';
                    } else if (exception === 'timeout') {
                        msg = 'Time out error.';
                    } else if (exception === 'abort') {
                        msg = 'Ajax request aborted.';
                    } else {
                        msg = 'Uncaught Error.\n' + jqXHR.responseText;
                    }
                    $('#ErrorMessageToUser').html(msg);
                    $("#ErrorMessageToUser").css("display", "block");
                }
            });
            break;
        case 'button_senders':
            $.ajax({
                url: '/Contacts/GetSendersList',
                type: "GET",
                success: function (data) {
                    $("#Contacts_ContentBlock").html(data);
                },
                error: function (jqXHR, exception) {
                    var msg = '';
                    if (jqXHR.status === 0) {
                        msg = 'Not connect.\n Verify Network.';
                    } else if (jqXHR.status == 404) {
                        msg = 'Requested page not found. [404]';
                    } else if (jqXHR.status == 500) {
                        msg = 'Internal Server Error [500].';
                    } else if (exception === 'parsererror') {
                        msg = 'Requested JSON parse failed.';
                    } else if (exception === 'timeout') {
                        msg = 'Time out error.';
                    } else if (exception === 'abort') {
                        msg = 'Ajax request aborted.';
                    } else {
                        msg = 'Uncaught Error.\n' + jqXHR.responseText;
                    }
                    $('#ErrorMessageToUser').html(msg);
                    $("#ErrorMessageToUser").css("display", "block");
                }
            });
            break;
        case 'button_addfriend':
            $.ajax({
                url: '/Contacts/AddFriendMenu',
                type: "GET",
                success: function (data) {
                    $("#Contacts_ContentBlock").html(data);
                },
                error: function (jqXHR, exception) {
                    var msg = '';
                    if (jqXHR.status === 0) {
                        msg = 'Not connect.\n Verify Network.';
                    } else if (jqXHR.status == 404) {
                        msg = 'Requested page not found. [404]';
                    } else if (jqXHR.status == 500) {
                        msg = 'Internal Server Error [500].';
                    } else if (exception === 'parsererror') {
                        msg = 'Requested JSON parse failed.';
                    } else if (exception === 'timeout') {
                        msg = 'Time out error.';
                    } else if (exception === 'abort') {
                        msg = 'Ajax request aborted.';
                    } else {
                        msg = 'Uncaught Error.\n' + jqXHR.responseText;
                    }
                    $('#ErrorMessageToUser').html(msg);
                    $("#ErrorMessageToUser").css("display", "block");
                }
            });
            break;
    }
};


